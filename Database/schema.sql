CREATE TABLE IF NOT EXISTS companies (
    company_id          INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name                VARCHAR(100) NOT NULL,
    fiscal_year_start   DATE NOT NULL,
    closing_day         INTEGER NOT NULL DEFAULT 31,
    tax_entry_method    VARCHAR(20) NOT NULL DEFAULT 'gross',
    is_tax_exempt       BOOLEAN NOT NULL DEFAULT FALSE,
    created_at          TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

ALTER TABLE companies ADD COLUMN IF NOT EXISTS closing_day INTEGER;
ALTER TABLE companies ADD COLUMN IF NOT EXISTS tax_entry_method VARCHAR(20);
ALTER TABLE companies ADD COLUMN IF NOT EXISTS is_tax_exempt BOOLEAN;
