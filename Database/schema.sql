WHERE closing_day IS NULL;
UPDATE companies
SET tax_entry_method = 'gross'
WHERE tax_entry_method IS NULL;
UPDATE companies
SET is_tax_exempt = FALSE
WHERE is_tax_exempt IS NULL;
