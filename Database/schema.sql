ALTER TABLE companies ALTER COLUMN closing_day SET DEFAULT 31;
ALTER TABLE companies ALTER COLUMN closing_day SET NOT NULL;
ALTER TABLE companies ALTER COLUMN tax_entry_method SET DEFAULT 'gross';
